    private void BtnConsultarUMAssentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConsultarUMAssentoActionPerformed
  
      if (CampoCodAss.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Informe o CodAssento!", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    String sql = "SELECT cod_assento, num_bilhete, data_emissao, _classe, valor_ass, _status, cod_pacote, cod_destino, cod_local_partida, cod_voo FROM Assento WHERE cod_assento = ?";

    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) TableAss.getModel();
    modelo.setRowCount(0);

    conexao.ConexaoMySQLSky conn_obj = new conexao.ConexaoMySQLSky();

    try (java.sql.Connection conn = conn_obj.conectar();
         java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, Integer.parseInt(CampoCodAss.getText()));

        try (java.sql.ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("cod_assento"),
                    rs.getInt("num_bilhete"),
                    rs.getString("data_emissao"),
                    rs.getString("_classe"),
                    rs.getDouble("valor_ass"),
                    rs.getBoolean("_status") ? "1" : "0",
                    rs.getInt("cod_pacote"),
                    rs.getInt("cod_destino"),
                    rs.getInt("cod_local_partida"),
                    rs.getInt("cod_voo")
                });
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Assento não encontrado!");
            }
        }

    } catch (java.sql.SQLException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
    }
        
    }//GEN-LAST:event_BtnConsultarUMAssentoActionPerformed

