private void removerAssento() {
    if (TableAss.getSelectedRow() < 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um assento na tabela!", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja remover?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

    String sql = "DELETE FROM Assento WHERE cod_assento = ?";

    try (java.sql.Connection conn = conexao.conectar();
         java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, Integer.parseInt(CampoCodAss.getText()));
        ps.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Assento removido!");
        limparCampos();
        consultarAssentos();

    } catch (java.sql.SQLException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
    }
}

