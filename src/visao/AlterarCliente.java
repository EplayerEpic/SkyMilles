    public void popularCombo() {
        ClientesControle cc = new ClientesControle();
        ArrayList<Clientes> clientes = cc.consultarClientes();
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);
        for (Clientes c : clientes) {
            modelo.addRow(new Object[]{
                c.getCliCodigo(),
                c.getCliNome(),
                c.getCliCPF(),
                c.getCliDataNasc() != null ? c.getCliDataNasc().toString() : "",
                c.getCliEndereco(),
                c.getCliTelefone(),
                c.getCliSexo() + ""
            });
        }

    }

