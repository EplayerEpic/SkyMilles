    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int linhaSelecionada = jTable2.getSelectedRow();
    if (linhaSelecionada < 0) {
        JOptionPane.showMessageDialog(null, "Selecione um cliente na tabela antes de alterar!");
        return;
    }
    try {
        ClientesControle cc = new ClientesControle();
        int codigo = (int) jTable2.getValueAt(linhaSelecionada, 0);
        Clientes cliOriginal = cc.consultarClienteCodigo(codigo);

        Clientes cli = new Clientes();
        cli.setCliCodigo(codigo);
        cli.setCliNome(jTable2.getValueAt(linhaSelecionada, 1).toString());
        cli.setCliCPF(jTable2.getValueAt(linhaSelecionada, 2).toString());
        cli.setCliDataNasc(java.time.LocalDate.parse(jTable2.getValueAt(linhaSelecionada, 3).toString()));
        cli.setCliEndereco(jTable2.getValueAt(linhaSelecionada, 4).toString());
        cli.setCliTelefone(jTable2.getValueAt(linhaSelecionada, 5).toString());
        cli.setCliSexo(cliOriginal.getCliSexo());

        String resposta = cc.alterarCliente(cli);
        if (resposta.equalsIgnoreCase("alterado")) {
            JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso");
            popularCombo();
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não alterado: Erro");
        }
    } catch (java.time.format.DateTimeParseException e) {
        JOptionPane.showMessageDialog(null, "Data inválida! Use o formato yyyy-MM-dd");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

