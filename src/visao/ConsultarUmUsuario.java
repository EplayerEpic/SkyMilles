    public void popularCombo() {
        jComboBox1.removeAllItems();
        ArrayList<Usuario> usuarios = uc.consultarUsuarios();
        int i = 0;
        for (Usuario u : usuarios) {
            jComboBox1.insertItemAt(u.getUsuCodigo() + " - " + u.getUsuLogin(), i);
            i++;
        }
    }

