    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(null, "Selecionar um Usuário");
        } else {
            int codigo = Integer.parseInt(jComboBox1.getSelectedItem().toString().split(" - ")[0]);
            Usuario usu = uc.consultarUsuarioCodigo(codigo);
            login.setText(usu.getUsuLogin());
            senha.setText(usu.getUsuSenha());
            email.setText(usu.getUsuEmail());
            codCliente.setText(usu.getUsuCodCliente() + "");
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

